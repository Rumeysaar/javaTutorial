package ch08_Loops;

public class C03_nested_forLoop {
    public static void main(String[] args) {

        for (int kat = 0; kat < 10; kat++) {
            System.out.println("kat = " + kat);
            for (int daire = 1; daire <= 4; daire++) {
                System.out.println("daire = " + daire);
            }
        }











    }
}
