package ch04_JavaOperators.jo01_ArithmeticOperators;

public class C02_IncrementDecrement {
    public static void main(String[] args) {

        int number = 7;
        System.out.println(++number);
        System.out.println("number = " + number);

        System.out.println(number++);

        System.out.println("number = " + number);

        System.out.println(number++  +  ++ number);

        System.out.println("number = " + number);

        System.out.println(number++ + number++);













    }
}
